function definir_parametros(parametros_a_sortear){
    for(let i=0;i<4;i++)
        sortear_parametro(parametros_a_sortear)    
}

function sortear_parametro(parametros_a_sortear){
    do{numero_random = Math.floor(Math.random()* parametros_a_sortear.length)}
    while(parametros_de_partida_designados.includes(parametros_a_sortear[numero_random]))
    parametros_de_partida_designados.push(parametros_a_sortear[numero_random])  
}

function sortear_equipo(equipos_a_sortear){
    equipo_a_adivinar = equipos_a_sortear[Math.floor(Math.random()* equipos_a_sortear.length)]
}
